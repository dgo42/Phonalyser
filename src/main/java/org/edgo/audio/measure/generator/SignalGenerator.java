    /**
     * Pink noise via the Voss–McCartney summed-octave algorithm.
     *
     * @param linear true  → uniformly distributed white source (flat amplitude PDF)
     *               false → Gaussian white source
     */
    private double pinkNoise(boolean linear) {
        pinkCounter++;
        int trailing = Long.numberOfTrailingZeros(pinkCounter);
        if (trailing < PINK_OCTAVES) {
            double newVal = linear ? (rng.nextDouble() * 2.0 - 1.0) : rng.nextGaussian();
            pinkRunningSum += newVal - pinkRows[trailing];
            pinkRows[trailing] = newVal;
        }
        double white = linear ? (rng.nextDouble() * 2.0 - 1.0) : rng.nextGaussian();
        return (pinkRunningSum + white) / (PINK_OCTAVES + 1.0);
    }

