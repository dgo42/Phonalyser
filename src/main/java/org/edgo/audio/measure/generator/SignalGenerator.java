    // DDS waveform kernels
    // -------------------------------------------------------------------------

    /**
     * DDS sine with 9th-order Taylor series correction (Horner-evaluated).
     *
     * Phase accumulator → top 16 bits select the lookup table entry (coarse angle θ₀),
     * bottom 16 bits carry the fractional phase error Δθ (in radians, ≤ 9.6e-5).
     *   sin(θ₀ + Δθ) = sin(θ₀)·cos(Δθ) + cos(θ₀)·sin(Δθ)
     * with
     *   cos(Δθ) ≈ 1 − Δθ²/2 + Δθ⁴/24 − Δθ⁶/720 + Δθ⁸/40320
     *   sin(Δθ) ≈ Δθ − Δθ³/6 + Δθ⁵/120 − Δθ⁷/5040 + Δθ⁹/362880
     *
     * <p>At TABLE_BITS=16 the Δθ ≤ 9.6e-5 rad bound makes order-5 truncation
     * already accurate to ~6.8e-23 (≈ −400 dB) — well below double-precision ε.
     * The order-9 form here is overkill for the current table size but costs
     * only a few flops and removes the Taylor correction from the error budget
     * if TABLE_BITS is ever reduced to shrink the lookup table.
     */
    private double ddsSine() {
        int    idx  = (int) (phaseAcc >>> (32 - TABLE_BITS));   // 0 .. 65535
        long   frac = phaseAcc & ((1L << (32 - TABLE_BITS)) - 1);
        double dx   = frac * TWO_PI_OVER_2_32;                  // fractional radians
        double dx2  = dx * dx;
        double cosDx = 1.0 + dx2 * (-0.5
                              + dx2 * (1.0 / 24.0
                              + dx2 * (-1.0 / 720.0
                              + dx2 * (1.0 / 40320.0))));
        double sinDx = dx * (1.0 + dx2 * (-1.0 / 6.0
                                  + dx2 * (1.0 / 120.0
                                  + dx2 * (-1.0 / 5040.0
                                  + dx2 * (1.0 / 362880.0)))));
        double sinT = SINE_TABLE[idx];
        double cosT = COS_TABLE[idx];
        return sinT * cosDx + cosT * sinDx;
    }

    /** Second DDS sine reader — identical math to {@link #ddsSine}, but
     *  reads {@link #phaseAcc2} instead of {@link #phaseAcc} so the
     *  dual-tone waveform can run two independent oscillators in lock-
     *  step with a single sample tick.  Kept as a separate method
     *  rather than parameterising {@code ddsSine} so the JIT can still
     *  inline the per-tone read without an extra parameter copy. */
    private double ddsSine2() {
        int    idx  = (int) (phaseAcc2 >>> (32 - TABLE_BITS));
        long   frac = phaseAcc2 & ((1L << (32 - TABLE_BITS)) - 1);
        double dx   = frac * TWO_PI_OVER_2_32;
        double dx2  = dx * dx;
        double cosDx = 1.0 + dx2 * (-0.5
                              + dx2 * (1.0 / 24.0
                              + dx2 * (-1.0 / 720.0
                              + dx2 * (1.0 / 40320.0))));
        double sinDx = dx * (1.0 + dx2 * (-1.0 / 6.0
                                  + dx2 * (1.0 / 120.0
                                  + dx2 * (-1.0 / 5040.0
                                  + dx2 * (1.0 / 362880.0)))));
        double sinT = SINE_TABLE[idx];
        double cosT = COS_TABLE[idx];
        return sinT * cosDx + cosT * sinDx;
    }

    /**
     * DDS rectangle / pulse train.  Output is {@code +1} for the first
     * {@link #rectDuty} fraction of the period, {@code −1} for the rest.
     * A 50 % duty cycle produces a symmetric square wave; other duty values
     * have a non-zero DC component which is the user's intent for pulse-
     * style test signals.
     */
    private double ddsRectangle() {
        double frac = (phaseAcc & 0xFFFFFFFFL) * (1.0 / 4294967296.0);   // [0, 1)
        return frac < rectDuty ? +1.0 : -1.0;
    }

    /**
     * DDS triangle with adjustable duty cycle.  The rising portion takes
     * {@link #triDuty} of the period (−1 → +1), the falling portion takes
     * the remainder (+1 → −1).  At {@code triDuty = 0.5} the waveform is a
     * symmetric triangle; near {@code 1.0} it approaches a sawtooth; near
     * {@code 0.0} it approaches an inverted sawtooth.  Piecewise-linear in
     * the phase domain — exact by construction.
     */
    private double ddsTriangle() {
        double frac = (phaseAcc & 0xFFFFFFFFL) * (1.0 / 4294967296.0);   // [0, 1)
        double d = triDuty;
        if (frac < d) {
            return frac / d * 2.0 - 1.0;
        } else {
            return 1.0 - (frac - d) / (1.0 - d) * 2.0;
        }
    }

