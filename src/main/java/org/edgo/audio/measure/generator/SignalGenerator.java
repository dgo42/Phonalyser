    /**
     * Initialises harmonic compensation phasors directly from a {@link FftResult}.
     * Equivalent to {@link #loadHarmonics} but avoids writing a CSV file.
     * Only harmonics whose bin index is valid (> 0) are loaded.
     */
    private void loadHarmonicsFromResult(double frequency, int sampleRate, FftResult r) {
        if (!r.coherentAveraging) {
            log.warn("FFT result uses incoherent averaging — phase information unavailable; "
                   + "phase correction disabled, amplitude correction only.");
        }

        // Fundamental phase: atan2(im, re) at the fundamental bin.
        // For incoherent results im[k] = 0, so phi1 = 0 → omegaD = -π/2 (no real correction).
        double phi1   = Math.atan2(r.im[r.fundamentalBin], r.re[r.fundamentalBin]);
        double omegaD = -(phi1 + Math.PI / 2.0);
        // Convert delay to rad/Hz so each harmonic uses its own actual frequency
        double delayRadPerHz = omegaD / r.fundamentalHzRefined;
        log.info("System delay estimate: {} rad ({} samples at {} Hz)",
                String.format(Locale.US, "%.2f", omegaD),
                String.format(Locale.US, "%.4f", omegaD / (2.0 * Math.PI * frequency / sampleRate)),
                (double) sampleRate);

        // Collect valid harmonics (H2 .. Hn)
        int validCount = 0;
        for (int h = 0; h < r.harmonicCount; h++) {
            if (r.harmonicBins[h] > 0) validCount++;
        }

        hAmpRatio = new double[validCount];
        hNum      = new int[validCount];
        hPhiInit  = new double[validCount];

        int i = 0;
        for (int h = 0; h < r.harmonicCount; h++) {
            int bin = r.harmonicBins[h];
            if (bin <= 0) continue;

            double phiH     = Math.atan2(r.im[bin], r.re[bin]);
            double ampRatio = r.harmonicPct[h] / 100.0;
            double freqHz   = r.harmonicHz[h];
            int    hNumber  = h + 2;

            double phiInit = phiH + freqHz * delayRadPerHz;
            hAmpRatio[i]   = ampRatio;
            hNum[i]        = hNumber;
            hPhiInit[i]    = phiInit;
            log.info("Harmonic compensation H{}: {} Hz  {} dBFS  phi_meas {} deg  phi_init {} deg",
                    hNumber,
                    String.format(Locale.US, "%.3f", freqHz),
                    String.format(Locale.US, "%.4f", 20.0 * Math.log10(ampRatio)),
                    String.format(Locale.US, "%.2f", Math.toDegrees(phiH)),
                    String.format(Locale.US, "%.2f", Math.toDegrees(phiInit)));
            i++;
        }
        log.info("Harmonic compensation: {} harmonic(s) loaded from FFT result", validCount);
    }

    /**
     * Parses an {@code fft_harmonics_*.csv} file and initialises the per-harmonic
     * rotating phasors for {@link GenSignalForm#SINE_COMPENSATED}.
     * Skips H1 (fundamental). For each H2..Hn entry the phasor is initialised from
     * the {@code re}/{@code im} columns when present (full double precision), otherwise
     * from {@code phase_deg}.  The correction amplitude is taken from
     * {@code amplitude_pct / 100} so it is automatically relative to the fundamental
     * regardless of the absolute DAC/ADC level.
     */
    private void loadHarmonics(double frequency, int sampleRate,
                                String csvPath) throws IOException {
        // [ampRatio, phi_measured (rad), freqHz, harmonicNumber]
        List<double[]> list = new ArrayList<>();
        double phi1 = Double.NaN;   // fundamental measured phase (rad) — used for delay compensation

        try (BufferedReader br = new BufferedReader(new FileReader(csvPath))) {
            String line;
            while ((line = br.readLine()) != null) {
                line = line.trim();
                // skip blank lines, comments, and the header row
                if (line.isEmpty() || !Character.isDigit(line.charAt(0))) continue;
                String[] cols = line.split(";");
                if (cols.length < 5) continue;
                int hIndex = Integer.parseInt(cols[0].trim());

                // Derive phase from re/im (full precision) or phase_deg column
                double phi;
                if (cols.length >= 7) {
                    double re  = Double.parseDouble(cols[5].trim().replace(',', '.'));
                    double im  = Double.parseDouble(cols[6].trim().replace(',', '.'));
                    phi = Math.atan2(im, re);
                } else {
                    phi = Math.toRadians(Double.parseDouble(cols[4].trim().replace(',', '.')));
                }

                if (hIndex == 1) {
                    phi1 = phi;   // capture fundamental phase for delay compensation
                    continue;
                }
                double ampPct = Double.parseDouble(cols[3].trim().replace(',', '.'));
                double freqHz = Double.parseDouble(cols[1].trim().replace(',', '.'));
                list.add(new double[]{ ampPct / 100.0, phi, freqHz, hIndex });
            }
        }

        if (Double.isNaN(phi1)) {
            // H1 row absent — assume DDS sine start phase (-π/2), delay unknown → no correction
            log.warn("H1 row not found in harmonics CSV — system-delay phase correction disabled");
            phi1 = -Math.PI / 2.0;
        }

        // System delay compensation:
        //   DDS sine at n=0 has FFT phase -π/2  →  phi1_measured = -ωD - π/2
        //   ωD = -(phi1_measured + π/2)
        //
        // For harmonic h the correction must arrive at the ADC with phase phi_h_measured.
        // It is sent from the DAC at phase phi_init and arrives delayed by h·ωD:
        //   phi_init - h·ωD = phi_h_measured
        //   phi_init = phi_h_measured + h·ωD = phi_h_measured - h·(phi1 + π/2)
        double omegaD = -(phi1 + Math.PI / 2.0);   // ωD in radians at fundamental
        double delayRadPerHz = omegaD / frequency;    // per-harmonic delay
        log.info("System delay estimate: {} rad ({} samples at {} Hz)",
                String.format(Locale.US, "%.2f", omegaD),
                String.format(Locale.US, "%.4f", omegaD / (2.0 * Math.PI * frequency / sampleRate)),
                (double) sampleRate);

        int n     = list.size();
        hAmpRatio = new double[n];
        hNum      = new int[n];
        hPhiInit  = new double[n];
        for (int i = 0; i < n; i++) {
            double[] h    = list.get(i);
            hAmpRatio[i]  = h[0];
            double phiH   = h[1];
            double freqHz = h[2];
            int    hNumber = (int) h[3];   // harmonic number: 2, 3, 4, …

            double phiInit = phiH + freqHz * delayRadPerHz;
            hNum[i]        = hNumber;
            hPhiInit[i]    = phiInit;
            log.info("Harmonic compensation H{}: {} Hz  {} dBFS  phi_meas {} deg  phi_init {} deg",
                    hNumber,
                    String.format(Locale.US, "%.3f", freqHz),
                    String.format(Locale.US, "%.4f", 20.0 * Math.log10(h[0])),
                    String.format(Locale.US, "%.2f", Math.toDegrees(phiH)),
                    String.format(Locale.US, "%.2f", Math.toDegrees(phiInit)));
        }
        log.info("Harmonic compensation: {} harmonic(s) loaded from {}", n, csvPath);
    }

    /**
     * DDS sine with H2..Hn harmonic compensation.
     *
     * Each harmonic's phase is derived by integer-multiplying the master 32-bit
     * phase accumulator by the harmonic number.  This locks the compensation
     * frequency exactly to h·f_DDS — no drift, no beating against the real
     * distortion harmonics, regardless of how many samples have been generated.
     */
    private double ddsSineCompensated() {
        double s = ddsSine();
        if (hAmpRatio != null) {
            for (int i = 0; i < hAmpRatio.length; i++) {
                long hPhase  = (phaseAcc * hNum[i]) & 0xFFFFFFFFL;
                double angle = hPhase * TWO_PI_OVER_2_32 + hPhiInit[i];
                s -= hAmpRatio[i] * Math.cos(angle);
            }
        }
        return s;
    }

